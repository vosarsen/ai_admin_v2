        // 4. data –º–æ–∂–µ—Ç –±—ã—Ç—å undefined –ø—Ä–∏ –æ—à–∏–±–∫–µ
        // 5. data.requiresServiceSelection - –Ω—É–∂–Ω–æ —É—Ç–æ—á–Ω–∏—Ç—å —É—Å–ª—É–≥—É
