    const today = new Date().toDateString();
    // –ï—Å–ª–∏ lastMessageDate –Ω–µ—Ç (—Å—Ç–∞—Ä—ã–π –∫–æ–Ω—Ç–µ–∫—Å—Ç), –ø—Ä–æ–≤–µ—Ä—è–µ–º lastActivity
