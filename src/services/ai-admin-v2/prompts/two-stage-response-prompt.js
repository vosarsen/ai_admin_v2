        // 1. data –∫–∞–∫ –º–∞—Å—Å–∏–≤ —Å–ª–æ—Ç–æ–≤ –Ω–∞–ø—Ä—è–º—É—é (two-stage)
        // 2. data.slots (ReAct mode)
